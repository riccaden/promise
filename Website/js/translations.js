// Get current language from localStorage, browser settings, or default to English
function getCurrentLanguage() {
    // Check if user has manually selected a language
    const savedLang = localStorage.getItem('oblivio_language');
    if (savedLang) {
        return savedLang;
    }

    // Auto-detect from browser language
    const browserLang = navigator.language || navigator.userLanguage; // e.g., "de-DE", "en-US", "it-IT", "ko-KR"
    const langCode = browserLang.split('-')[0].toLowerCase(); // Extract language code: "de", "en", "it", "ko"

    // Check if we support this language
    const supportedLanguages = ['en', 'de', 'it', 'ko'];
    if (supportedLanguages.includes(langCode)) {
        console.log('Auto-detected language from browser:', langCode);
        return langCode;
    }

    // Default to English if browser language is not supported
    console.log('Browser language not supported, defaulting to English');
    return 'en';
}

// Set language
function setLanguage(lang) {
    localStorage.setItem('oblivio_language', lang);
    updatePageLanguage();
}

// Get translation
function t(key) {
    const lang = getCurrentLanguage();
    return translations[lang][key] || translations['en'][key] || key;
}

// Update all elements with data-i18n attribute
function updatePageLanguage() {
    const lang = getCurrentLanguage();

    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = translations[lang][key];

        if (translation) {
            // Check if it's an input placeholder
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translation;
            } else if (element.innerHTML.includes('<')) {
                // If element contains HTML, use innerHTML
                element.innerHTML = translation;
            } else {
                // Otherwise use textContent
                element.textContent = translation;
            }
        }
    });

    // Update language selector dropdown
    const selector = document.getElementById('languageSelector');
    if (selector) {
        selector.value = lang;
    }

    // Update HTML lang attribute
    document.documentElement.lang = lang;
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    // Update page with current language
    updatePageLanguage();

    // Add event listener to language selector
    const languageSelector = document.getElementById('languageSelector');
    if (languageSelector) {
        languageSelector.addEventListener('change', function() {
            setLanguage(this.value);
        });
    }
});
